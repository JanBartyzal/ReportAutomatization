package com.reportplatform.snow.model.dto;

import java.time.Instant;
import java.util.List;
import java.util.UUID;

public class DistributionHistoryDTO {

    private UUID id;
    private UUID orgId;
    private UUID ruleId;
    private List<String> recipients;
    private String reportBlobUrl;
    private String status;
    private Instant sentAt;
    private String errorMessage;
    private Instant createdAt;

    public UUID getId() {
        return id;
    }

    public void setId(UUID id) {
        this.id = id;
    }

    public UUID getOrgId() {
        return orgId;
    }

    public void setOrgId(UUID orgId) {
        this.orgId = orgId;
    }

    public UUID getRuleId() {
        return ruleId;
    }

    public void setRuleId(UUID ruleId) {
        this.ruleId = ruleId;
    }

    public List<String> getRecipients() {
        return recipients;
    }

    public void setRecipients(List<String> recipients) {
        this.recipients = recipients;
    }

    public String getReportBlobUrl() {
        return reportBlobUrl;
    }

    public void setReportBlobUrl(String reportBlobUrl) {
        this.reportBlobUrl = reportBlobUrl;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public Instant getSentAt() {
        return sentAt;
    }

    public void setSentAt(Instant sentAt) {
        this.sentAt = sentAt;
    }

    public String getErrorMessage() {
        return errorMessage;
    }

    public void setErrorMessage(String errorMessage) {
        this.errorMessage = errorMessage;
    }

    public Instant getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(Instant createdAt) {
        this.createdAt = createdAt;
    }
}
